package de.wathoserver.vaadin.visjs.network.util;

/**
 * The shape defines what the node looks like. There are two types of nodes. One type has the label
 * inside of it and the other type has the label underneath it.
 *
 * The types with the label inside of it are: ellipse, circle, database, box, text. The ones with
 * the label outside of it are: image, circularImage, diamond, dot, star, triangle, triangleDown,
 * hexagon, square and icon.
 *
 * @author watho
 *
 */
public enum Shape {
  ellipse, circle, box, image, circularImage, diamond, dot, star, triangleDown, triangle, square, icon, database, text;
}
